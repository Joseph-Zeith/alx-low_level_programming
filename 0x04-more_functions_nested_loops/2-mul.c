#include "main.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * mul - multiplies two integers
 * Description: gets two integers and multiplies them to get the result
 * @a: first integer to multiply
 * @b: second integer to multiply
 *
 * Return: result of the multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
